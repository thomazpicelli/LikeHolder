DROP TABLE CATEGORIA_INTERESSE;
DROP TABLE USUARIO;
DROP TABLE ARTISTA;
DROP TABLE MUSICA;
DROP TABLE EVENTO;
DROP TABLE NIVEL_ACESSO;

CREATE TABLE USUARIO(
  CD_USUARIO INT NOT NULL PRIMARY KEY GENERATED ALWAYS AS IDENTITY(START WITH 1, INCREMENT BY 1),
  NM_USUARIO VARCHAR(50) NOT NULL,
  DC_EMAIL VARCHAR(50) NOT NULL,
  NM_USERNAME VARCHAR(50) NOT NULL,
  NM_PASSWORD VARCHAR(10) NOT NULL,
  NM_NIVEL VARCHAR(10) NOT NULL
);

CREATE TABLE NIVEL_ACESSO(
  CD_NIVEL INT NOT NULL PRIMARY KEY GENERATED ALWAYS AS IDENTITY(START WITH 1, INCREMENT BY 1),
  NM_NIVEL VARCHAR(15) NOT NULL
);

CREATE TABLE CATEGORIA_INTERESSE(
  CD_CATEGORIA_INTERESSE INT NOT NULL PRIMARY KEY GENERATED ALWAYS AS IDENTITY(START WITH 1, INCREMENT BY 1),
  NM_CATEGORIA_INTERESSE VARCHAR(50) NOT NULL
);

CREATE TABLE ARTISTA(
    CD_ARTISTA INT NOT NULL PRIMARY KEY GENERATED ALWAYS AS IDENTITY(START WITH 1, INCREMENT BY 1),
    CD_USUARIO INT NOT NULL,
    DC_ID VARCHAR(255) NOT NULL,
    DC_URL VARCHAR(255) NOT NULL,
    NM_ARTISTA VARCHAR(255) NOT NULL,
    CD_FOLLOWER INT,
    DC_IMG VARCHAR(500),
    CD_HEIGHT INT,
    CD_WIDTH INT
);

CREATE TABLE MUSICA(
    CD_MUSICA INT NOT NULL PRIMARY KEY GENERATED ALWAYS AS IDENTITY(START WITH 1, INCREMENT BY 1),
    CD_USUARIO INT NOT NULL,
    DC_ID VARCHAR(255) NOT NULL,
    DC_URL VARCHAR(255) NOT NULL,
    DC_URI VARCHAR(255) NOT NULL,
    NM_MUSICA VARCHAR(255) NOT NULL,
    NM_ARTISTA VARCHAR(255) NOT NULL
);

CREATE TABLE EVENTO(
    CD_EVENTO INT NOT NULL PRIMARY KEY GENERATED ALWAYS AS IDENTITY(START WITH 1, INCREMENT BY 1),
    CD_USUARIO INT NOT NULL,
    DC_ID VARCHAR(255) NOT NULL,
    DC_URL VARCHAR(255) NOT NULL,
    DC_TICKET VARCHAR(255) NOT NULL,
    DC_LOCAL VARCHAR(255) NOT NULL,
    DC_CIDADE VARCHAR(255) NOT NULL
);

